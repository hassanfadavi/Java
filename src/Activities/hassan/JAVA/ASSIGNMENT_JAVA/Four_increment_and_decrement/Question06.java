
package Activities.hassan.JAVA.ASSIGNMENT_JAVA.Four_increment_and_decrement;

public class Question06 {
    public static void main(String[] args) {



//Question 6: Pre-Decrement
//
//Task:
//Given an int variable d = 20, use pre-decrement (--d) and then print d.
// Explain how pre-decrement works compared to post-decrement.
//
//Expected Output:
//
//19


        int d=22;

        --d; //21
        d--; //20

        System.out.println( d);  //20
        System.out.println(d--);   //20 and then 19 will be printed for the next print
        System.out.println(d);   //19
        System.out.println(--d);  //18



}
}
