package Activities.hassan.JAVA.ASSIGNMENT_JAVA.C_Loop;

public class C3_Loop_with_Conditional {
    public static void main(String[] args) {


  //3. Loop with Conditional:
     //Using a for loop, display numbers from 1 to 50,
        // but skip any number that is divisible by 5.

        for(int i=0;i<=50;i++)
            if(i%5!=0){
                System.out.print(i+",");
            }

    }
}
