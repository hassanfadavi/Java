public class test2 {


    //  1)no parameters, no return values
    void m1() {
        System.out.println("hello");
    }

    //  2)no parameters,  return values
    String m2() {
        return "hi";
    }


    //3)take parameters, no return values
    void m3(String name) {
        System.out.println("hi " + name);
    }



    //4)take parameters,  return values
    String m4(String name) {
        return ("hi" + name);
    }
}


















